public class Exercicio9 {
    public static void main(String[] args) {

        int contador = 0;

        System.out.println("Valor inicial: " + contador);
        System.out.println("contador++: " + (contador++));
        System.out.println("Valor ap贸s: " + contador);

        System.out.println("++contador: " + (++contador));
        System.out.println("Valor ap贸s: " + contador);

        System.out.println("contador--: " + (contador--));
        System.out.println("Valor ap贸s: " + contador);

        System.out.println("--contador: " + (--contador));
        System.out.println("Valor ap贸s: " + contador);
    }
}
