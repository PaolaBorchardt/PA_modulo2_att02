public class Exercicio10 {
    public static void main(String[] args) {

        int resultado1 = 2 + 3 * 4;
        System.out.println("2 + 3 * 4 = " + resultado1);

        int resultado2 = (2 + 3) * 4;
        System.out.println("(2 + 3) * 4 = " + resultado2);

        int resultado3 = 10 + 5 * 2 - 3;
        System.out.println("10 + 5 * 2 - 3 = " + resultado3);

        int resultado4 = (10 + 5) * (2 - 3);
        System.out.println("(10 + 5) * (2 - 3) = " + resultado4);
    }
}
